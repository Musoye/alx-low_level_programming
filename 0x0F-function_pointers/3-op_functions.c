#include <stdio.h>

/**
* op_add - adds numbers
* @a: an integer
* @b: an integer
* Return: an integer
*/
int op_add(int a, int b)
{
  return (a + b);
}

/**
* op_sub - subtracts numbers
* @a: an integer
* @b: an integer
* Return: an integer
*/
int op_sub(int a, int b)
{
  return (a - b);
}

/**
* op_mul - multiplies numbers
* @a: an integer
* @b: an integer
* Return: an integer
*/
int op_mul(int a, int b)
{
  return (a * b);
}

/**
* op_div - divides numbers
* @a: an integer
* @b: an integer
* Return: an integer
*/
int op_div(int a, int b)
{
  return (a / b);
}

/**
* op_mod - finds remainder of division of numbers
* @a: an integer
* @b: an integer
* Return: an integer
*/
int op_mod(int a, int b)
{
  return (a % b);
}
